package com.example.absensi;

public class Kelas {
    private int id;
    private String namakel;
    private String namawali;
    private String namajur;
    private String kaprog;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNamakel() {
        return namakel;
    }

    public void setNamakel(String namakel) {
        this.namakel = namakel;
    }

    public String getNamawali() {
        return namawali;
    }

    public void setNamawali(String namawali) {
        this.namawali = namawali;
    }

    public String getNamajur() {
        return namajur;
    }

    public void setNamajur(String namajur) {
        this.namajur = namajur;
    }

    public String getKaprog() {
        return kaprog;
    }

    public void setKaprog(String kaprog) {
        this.kaprog = kaprog;
    }
}
